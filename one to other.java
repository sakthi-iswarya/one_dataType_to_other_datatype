//int to double 
import java.util.*;
class Main{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int x =sc.nextInt();
        double x1 =(double) x;
        System.out.println(x1);
        double e=sc.nextDouble();
        int e2 =(int) e;
        System.out.println(e2);
    }
}
